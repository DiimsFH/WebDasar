<?php
/*
###############################################################
#   Looping                                                   #
#   1. Counted Loop (for, foreach)                            #
#   2. Uncounted loop (while, do while)                       #
###############################################################
*/

//-------------------------------------------------------------------Counted Loop--------------------------------------------------------------------//
                                               //-------------------------FOR-------------------------//
    for($i=1; $i<=6; $i++){
        echo "Nilai i = $i <br>";
    }

    for($i=6; $i>=1; $i--){
        echo "<h$i>Heading $i</h$i>";
    }
                                                //-----------------------FOREACH----------------------//
    $prodi = ['TKOM','TRPL','MI','ANIM'];
    
    foreach($prodi as $ass){
        echo "<p>$ass</p>";
    }
//-------------------------------------------------------------------Uncounted Loop--------------------------------------------------------------------//
                                                //-----------------------While-------------------------//
                                                // Mikir dulu baru kerjakan
    $x = 10;
    while($x<=6){
        echo "Nilai x = $x <br>";
        $x++;
    }
                                                //----------------------Do While------------------------//
                                                //kerja dulu baru
    $y = 10;
    do{
        echo "Nilai y = $y <br>";
        $y++;
    }while($y<=6);
?>